x = 10
y = 2

def calculator(a, b):
    """Docstring"""
    print('Addition ->', a + b)
    print('Subtraction ->', a - b)
    print('Division ->', a / b)
    print('Multiplication ->', a * b)
    print('Exponent (Power) ->', a ** b)
    print('Modulus ->', a % b)
    print('Floor division ->', a // b)

calculator(x, y)